package builder;

public class Vehiculo {

    public String marca;
    public String tipo;
    public String motor;
    public int puertas;

    public String getMarca() {
        return marca;
    }


    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getMotor() {
        return motor;
    }



    public int getPuertas() {
        return puertas;
    }


}
